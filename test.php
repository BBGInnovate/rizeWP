<p>ACCRA, GHANA — Inspired by hopes of a greener future, more inclusive communities and the smiles of young children, Cordie Aziz and her team at <a href="http://www.environment360gh.org/">Environment360</a> are working to improve the lives of children in Accra’s coastal and mining communities, where poor environmental and sanitary conditions abound.</p>
<blockquote><p>“People don’t think of trash as a viable business. Most people don’t even want to be associated with it. But trash is a multimillion dollar industry. We think it is really time for young Ghanaians to realize that they are sitting on a gold mine. Of course, we want to teach children that it is important to wash their hands and to use the toilets properly but that they can also leverage what is right in front of them. They should be asking themselves how can I convert trash into money and use it to lift my family from one level to another level, and still solve one of the city’s most critical issues?”<br />
<span class="attribution">– Cordie Aziz</span></p></blockquote>
<div id="attachment_1252" class="wp-caption alignnone"><a href="https://africa.rizing.org/wp-content/uploads/2015/10/Cordie-Aziz-works-out-of-a-small-office-in-DzorwuluPhoto-cred-Eunice.jpg"><img class="size-large wp-image-1252" src="https://africa.rizing.org/wp-content/uploads/2015/10/Cordie-Aziz-works-out-of-a-small-office-in-DzorwuluPhoto-cred-Eunice-300x200.jpg" alt="Cordie Aziz works out of a small office in Dzorwulu." width="700" height="467" /></a><p class="wp-caption-text">Founder and Chair of Environment360, Cordie Aziz works from a small office in Dzorwulu.</p></div>
<p>The one-and-a-half-year-old NGO provides fun, educational opportunities for children between the ages of 6 and 13 that teach them about the negative health implications associated with open defecation, improper hand washing, air and water pollution, as well as the importance of recycling plastic waste in their communities.</p>
<p>In collaboration with the <a href="https://www.facebook.com/USEmbassyGhana/?fref=ts">US Embassy of Ghana</a> and Voltic Ghana, Environment360 said it plans to launch the <em>Growing Green</em> pilot program in November. The nine months-long program&#8217;s goal is to bring comprehensive environmental education to primary schools in Abelemkpe, Dzorwulu, Jamestown and Labadi.</p>
<blockquote><p>“When the children participate in National Sanitation Day which is the first Saturday of every month, they will then earn a ticket to go on a field trip. So, the three following Saturdays we will take them out on the trips and they will learn about biodiversity. Field trips have a major impact on these children’s lives.” – Cordie Aziz</p></blockquote>
<p>The <em>Growing Green</em> program plans to feature art, writing, dance and drama contests, along with quizzes designed to help students share their ideas of what a sustainable, local ecosystem looks like. Children who participate in the program will also have the chance to join gardening and environmental clubs, and participate in intensive recycling campaigns.</p>
<p>One of the program&#8217;s other goals is for the children to share what they&#8217;ve learned with their parents. Aziz said Environment360 will measure the impact of the program using qualitative surveys and by observing the communities they will be working in.</p>
<blockquote><p>“We don’t want to just change the environmental behaviors of these children. There must be a visible impact in the communities around these children. At the end of the program, we will come in and analyze. What are the new attitudes in the community?”</p></blockquote>
<p>Last year, Environment360 worked with 3,200 children, with 43 % of them coming from underserved communities around Accra. But Aziz said, making a lasting impact in the community is far more rewarding than just growing numbers of participants and any short-term changes. This year, she said, her organization has adopted some new, scalable strategies to get the job done.</p>
<blockquote><p>“Last year, we went into schools and engaged them in one-day seminars but we realized that the teachers in these public schools felt overworked. So for them, when you come in and do something like this, it’s like a break time for them. They were not really engaging; the teachers were not sitting in which means the lessons couldn’t be reinforced later. This year we will be working directly with the assemblyman which is the best way forward. Number one, he wants a project that is going to be successful. Number two, he is from the community so he has connections that we just don’t have and then, he is instrumental in convincing people to do things.”</p></blockquote>
<div id="attachment_1268" class="wp-caption alignnone"><a href="https://africa.rizing.org/wp-content/uploads/2015/10/Children-work-with-recycled-materials-at-the-Float-Your-Boat-Fundraiser-earlier-this-month-Photo-Cred-Environment-360.jpg"><img class="size-full wp-image-1268" src="https://africa.rizing.org/wp-content/uploads/2015/10/Children-work-with-recycled-materials-at-the-Float-Your-Boat-Fundraiser-earlier-this-month-Photo-Cred-Environment-360.jpg" alt="Children work with recycled materials at the Float Your Boat Fundraiser. (courtesy Environment 360)" width="535" height="730" /></a><p class="wp-caption-text">Children work with recycled materials at the Environment360 &#8216;Float Your Boat Fundraiser&#8217;. (Courtesy of Environment 360)</p></div>
<p>Currently, <a href="http://www.indexmundi.com/ghana/age_structure.html">nearly half of Ghana&#8217;s population is below the age of 24</a> and Environment360 faces the daunting task of equipping this growing demographic with the right tools to take on the resiliency challenges they face. Despite these challenges, Aziz said she remains hopeful,</p>
<blockquote><p>“we believe children are powerful change agents and the earlier we catch them, the easier it is to change their behavioral patterns. Kids in these deprived areas only need exposure, education and encouragement to know that they can fight the odds.”</p></blockquote>
<div id="featured-section">
<h3 class="featured-title">More on Environment360</h3>
<p style="text-align: left;">In May, Environment360 was awarded the 2015 UNICEF Reach for Change award as part of the <a href="http://ghana.reachforchange.org/en/programs/iimagineghana">iIMAGINEghana Challenge</a>. To learn more about the organization’s initiatives including their <a href="http://www.environment360gh.org/#!about1/cqf6">environmental excursions</a>, find them on <a href="https://www.facebook.com/environment360/timeline">Facebook page</a> online at <a href="http://www.environment360gh.org/">Environment360GH.org</a> and on Twitter, <a href="http://www.twitter.com/greenghana360">@greenghana360</a>.</p>
